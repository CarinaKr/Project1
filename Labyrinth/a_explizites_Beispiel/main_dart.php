<h2>Dart</h2>
<p>So funktioniert's: </p>
<p>Das Ziel ist es mindestens 20 Punkte zu erreichen. <br/>
Du hast 3 Pfeile. <br/>
Der Pfeil beginnt am oberen Rand und fällt nach unten, bis du eine Maustaste drueckst.<br/>
Mit der linken Maustaste drehst du den Pfeil gegen den Uhrzeigersinn. Mit der rechten 
Maustaste drehst du den Pfeil mit dem Uhrzeigersinn. <br/>
Dies ist auch möglich während der Pfeil fliegt.<br/>
Triffst du das Weiße der Scheibe, bekommst du einen Punkt. Triffst du das Gelbe, bekommst du
10 Punkte.<br/>
</p></br>
<p>Viel Erfolg!</p> <br/>